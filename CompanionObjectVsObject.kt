##Companion Object
/* 1. Should be defined inside the class only
2. Can be treated static keyword in java
3. Can be defined without name and can get called --> ClassName.CompanionMethodName()
4. Instantiated at the first time when the class is instantiated and this object is common for all instances of the class
*/
##Object
/*
1. Can be defined anywhere
2. Can be used for singleton behavior
3. Cannot be defined without name
4. Lazily instantiated when used for the first time.
*/
